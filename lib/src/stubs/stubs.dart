/// Magic CLI stub templates.
///
/// All stub content is defined as raw Dart string constants using `r'''...'''`
/// syntax. Placeholder tokens use `{{ name }}` convention.
///
/// Available placeholders (per stub):
/// - `{{ className }}` — PascalCase class name (e.g. `MonitorController`)
/// - `{{ snakeName }}` — snake_case resource name (e.g. `monitor`)
/// - `{{ tableName }}` — plural snake_case table name (e.g. `monitors`)
/// - `{{ resourceName }}` — API endpoint segment (e.g. `monitors`)
/// - `{{ modelName }}` — model class name (e.g. `Monitor`)
/// - `{{ camelName }}` — camelCase variable name (e.g. `monitor`)
/// - `{{ fullName }}` — migration full name (e.g. `2026_01_01_create_monitors_table`)
/// - `{{ eventClass }}` — event class name (e.g. `UserLoggedInEvent`)
/// - `{{ eventSnakeName }}` — event snake name (e.g. `user_logged_in_event`)
/// - `{{ policyName }}` — policy resource name (e.g. `monitor`)
/// - `{{ description }}` — human-readable description
/// - `{{ actionDescription }}` — action description for requests
